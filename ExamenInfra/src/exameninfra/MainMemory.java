    int cantFrames;
    
    public void crearFrames(int cantMemoria, int tamano_frames){
        cantFrames = cantMemoria/tamano_frames;
        System.out.println("Frames creados: "+cantFrames);
        for(int i =0;i<cantFrames;i++){
            JPanel pnl = new JPanel();
            pnl.setBounds(5, 5, 45, 45);
            pnl.setBorder(javax.swing.BorderFactory.createMatteBorder(1, 1, 1, 1, new java.awt.Color(0, 0, 51)));
            JLabel lblFrame = new JLabel(""+i);
            JLabel lblProceso = new JLabel("");
            JLabel lblPagina = new JLabel("");
            pnl.add(lblFrame);
            pnl.add(lblProceso);
            pnl.add(lblPagina);
            lblFrame.setLocation(2, 2);
            lblProceso.setLocation(2, 18);
            lblFrame.setLocation(2, 33);
            pnlPrincipal.add(pnl);
        }
    }

